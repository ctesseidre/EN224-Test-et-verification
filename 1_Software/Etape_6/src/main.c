#include "pgcd.h"

/*-------------------------------------------------------------------------------------------------------------------------------------------//
****************************************************Main***************************************************************************************
//-------------------------------------------------------------------------------------------------------------------------------------------*/
int main (int argc, char * argv []){
	printf("(II) Starting program\n");

    // Same value
	assert(PGCD(5, 5) == 5);
	// Normal values
    assert(PGCD(8, 6) == 2);
	// Same value equal to 0
    assert(PGCD(0, 0) == 0);
	// Valeur max
    assert(PGCD(65535, 5) == 5);
	// A = 0
    assert(PGCD(0, 5) == 5);
    //B negativ
    assert(PGCD(5, -6) == 5);

	printf("(II) End of program\n");

  return 0;
}