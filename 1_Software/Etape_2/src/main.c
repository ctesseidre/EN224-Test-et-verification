//-------------------------------------------------------------------------------------------------------------------------------------------//
int PGCD(int A, int B)
{
  if(A > 0 && B > 0){
    while(A!=B){
      if(A>B){
        A = A - B;
      } else {
        B = B - A;
      }
    }
  }
	return A;
}
